public class Aufgabe1 {

   public static void main(String[] args) {
   
      // Zuweisung der zwei ganzen Zahlen zu je einer Variablen
      int a = 5;
      int b = 3;

      // Ausführen der einzelnen Berechnungen
      System.out.println("Addition von " + a + " und " + b + " = " + (a+b));
      System.out.println("Substraktion von " + a + " und " + b + " = " + (a-b));
      System.out.println("Division von " + a + " und " + b + " = " + (a/b));
      System.out.println("Multiplikation von " + a + " und " + b + " = " + (a*b));
   }
}